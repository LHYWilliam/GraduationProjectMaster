  LCD_Printf(&LCD, 64, 64, "Hello");
  LCD_FillArea(&LCD, 16, 16, 32, 32);
  LCD_DrawLine(&LCD, 0, 0, 32, 32);
  LCD_DrawVLine(&LCD, 32, 64, 128);
  LCD_DrawHLine(&LCD, 16, 128, 32);
  LCD_DrawRectangle(&LCD, 32, 128, 3, 3);
  LCD_DrawCircle(&LCD, 48, 48, 16);



  /* Infinite loop */
